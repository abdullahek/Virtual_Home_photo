    function sendContactUsMail()
    {
        let name = $('#_name').val();
        let email = $('#_email').val();
        let phone = $('#_phone').val();
        let company = $('#_company').val();
        let message = $('#_message').val();
        
        if(name !== '' && email !== '' && phone !== '' && company !== '' && message !== ''){
            $.ajax({
                url:'handler.php',
                method: 'post',
                data: {name: name, email : email, phone : phone, company : company, message : message},
                success: function(response){
                    if(response === 'Success'){
                        $('#success').show();
                        $('#error').hide();
                    }
                    else
                    {
                        $('#error').show();
                        $('#success').hide();                        
                    }
                }
            });
        }
        else
        {
            alert("Some fields are missing!");
        }
    }
